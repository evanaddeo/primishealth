/**
 * Per-user data retention configuration.
 * One row per user; `user_id` is both the primary key and FK to `users`.
 *
 * raw_payload_retention_mode / ai_context_retention_mode allowed values:
 *   'extended' | 'standard' | 'minimal'
 */
export interface DataRetentionPreferencesTable {
  /** PK and FK to `users.id` (1-to-1 relationship). */
  user_id: string;
  /**
   * Raw provider payload retention policy.
   * Allowed: 'extended' | 'standard' | 'minimal'
   */
  raw_payload_retention_mode: Generated<string>;
  /**
   * Explicit retention window in days; null means policy-based (mode-driven).
   */
  raw_payload_retention_days: NullableCol<number>;
  /** Normalized/relational data retention policy. Default 'keep_until_deleted'. */
  normalized_data_retention_mode: Generated<string>;
  /**
   * AI context/snapshot retention policy.
   * Allowed: 'extended' | 'standard' | 'minimal'
   */
  ai_context_retention_mode: Generated<string>;
  /** Whether the algorithm engine may reprocess this user's data. */
  allow_algorithm_reprocessing: Generated<boolean>;
  updated_at: UpdatedAt;
}

