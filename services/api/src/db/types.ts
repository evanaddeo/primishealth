/**
 * Core application identity.
 *
 * Key design points:
 *   - `id` is the internal UUID — the primary key for all health-data FK references
 *     (ARCH-AUTH-001). Never expose `id` directly to clients.
 *   - `cognito_sub` is stored as a separate unique text field. It is the lookup key
 *     used by the auth middleware to resolve internal user identity (ARCH-AUTH-002).
 *   - `sex_at_birth`, `date_of_birth`, and `height_cm` are optional user-controlled
 *     biometrics. Never infer or require these.
 */
export interface UsersTable {
  /** Internal UUID primary key; generated by `gen_random_uuid()`. */
  id: UuidPk;
  /** Cognito `sub` claim from the user's ID token. Unique across all users. */
  cognito_sub: string;
  /** Primary email address; null until verified or provided during sign-up. */
  email: NullableCol<string>;
  /** Whether the email address has been verified via Cognito. */
  email_verified: Generated<boolean>;
  /** Display name shown in the app. */
  display_name: NullableCol<string>;
  /**
   * Account lifecycle status.
   * Allowed values: 'active' | 'suspended' | 'deletion_requested' | 'deleted'
   */
  status: Generated<string>;
  /** IANA timezone string for the user's primary local zone, e.g. 'America/New_York'. */
  primary_timezone: Generated<string>;
  /**
   * Date of birth in ISO format (YYYY-MM-DD), returned as string by node-postgres.
   * Optional; do not require or infer.
   */
  date_of_birth: NullableCol<string>;
  /**
   * Biological sex at birth — user-controlled, optional.
   * Do not infer demographic data.
   */
  sex_at_birth: NullableCol<string>;
  /**
   * Height in centimetres (numeric(6,2)); returned as string by node-postgres.
   * Optional; used to improve VO2/body-composition interpretation.
   */
  height_cm: NullableCol<string>;
  created_at: CreatedAt;
  updated_at: UpdatedAt;
  /** Set when user requests deletion; null for active accounts. */
  deleted_at: NullableCol<Date>;
}

/** Full row shape returned by SELECT queries on `users`. */
export type User = Selectable<UsersTable>;
/** Shape accepted by INSERT queries on `users`. */
export type NewUser = Insertable<UsersTable>;
/** Shape accepted by UPDATE queries on `users`. */
export type UserUpdate = Updateable<UsersTable>;

