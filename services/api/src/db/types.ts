/**
 * The root Kysely `Database` interface mapping table names to their row types.
 *
 * Pass this as the generic parameter when constructing the Kysely instance:
 *
 * ```typescript
 * import { Kysely } from 'kysely';
 * import type { Database } from './types.js';
 *
 * const db = new Kysely<Database>({ dialect });
 * ```
 *
 * The `schema_migrations` table is included so the migration runner can perform
 * type-safe inserts and selects when tracking applied migration versions.
 */
export interface Database {
  schema_migrations: SchemaMigrationsTable;

  // CU-027: Identity, preferences, and consent
  users: UsersTable;
  auth_identities: AuthIdentitiesTable;
  user_goals: UserGoalsTable;
  coach_preferences: CoachPreferencesTable;
  nutrition_philosophy_preferences: NutritionPhilosophyPreferencesTable;
  consent_records: ConsentRecordsTable;
  data_retention_preferences: DataRetentionPreferencesTable;
}

