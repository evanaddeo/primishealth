/**
 * Kysely `Database` interface — the central registry of all typed table definitions.
 *
 * Each schema CU adds table interfaces here:
 *   - CU-027: identity, preferences, consent tables
 *   - CU-028: provider connection and sync tables
 *   - CU-029: metric registry and observation tables
 *   - CU-030: domain tables (sleep, activity, body, manual, nutrition)
 *   - CU-031: score, insight, AI, and dashboard tables
 *
 * The `Database` interface is intentionally maintained by hand rather than generated
 * by a tool like `kysely-codegen`. The SQL migration files in `database/migrations/`
 * are the canonical schema source. Types here must accurately reflect those files.
 * See ADR-003 for the rationale.
 *
 * Internal-use table: `schema_migrations` is tracked here as a `SchemaMigrationsTable`
 * so the migration runner can query it in a type-safe manner.
 *
 * Column type conventions used throughout this file:
 *   - `Generated<T>`     — column has a DB default; T is required on SELECT,
 *                          optional on INSERT, and T on UPDATE. Imported from kysely.
 *   - `UuidPk`           — UUID primary key with gen_random_uuid() default;
 *                          optional on INSERT, never updated.
 *   - `CreatedAt`        — timestamptz with now() default; optional on INSERT,
 *                          immutable after creation (never update).
 *   - `UpdatedAt`        — timestamptz with now() default; optional on INSERT,
 *                          set explicitly by repository write methods (D-A-008).
 *   - `NullableCol<T>`   — nullable column; T | null on SELECT and UPDATE,
 *                          optional (T | null | undefined) on INSERT.
 *
 * Note on `exactOptionalPropertyTypes: true`:
 *   Kysely's `Insertable<T>` and `Updateable<T>` helpers generate correct optional
 *   property types. Repositories should use `Insertable<XTable>` and `Updateable<XTable>`
 *   rather than hand-writing `field?: T | undefined` in function signatures.
 */

import type { ColumnType, Generated, Selectable, Insertable, Updateable } from 'kysely';

// ---------------------------------------------------------------------------
// Shared column-type aliases
// ---------------------------------------------------------------------------

/** UUID primary key with `gen_random_uuid()` default. Never updated. */
type UuidPk = ColumnType<string, string | undefined, never>;

/** `created_at timestamptz not null default now()` — immutable after creation. */
type CreatedAt = ColumnType<Date, Date | undefined, never>;

/**
 * `updated_at timestamptz not null default now()` — set explicitly by
 * repository write methods on every mutation (D-A-008, no DB trigger).
 */
type UpdatedAt = Generated<Date>;

/**
 * Nullable column that may be omitted on INSERT (becomes NULL in the DB).
 * Maps to `T | null` on SELECT/UPDATE and `T | null | undefined` on INSERT
 * so Kysely's `Insertable<T>` renders the field as optional.
 */
type NullableCol<T> = ColumnType<T | null, T | null | undefined, T | null>;

// ---------------------------------------------------------------------------
