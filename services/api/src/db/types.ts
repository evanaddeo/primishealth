/**
 * Links sign-in methods to a Primis user account.
 *
 * IMPORTANT: `provider` values represent app authentication methods only.
 * Allowed: 'email_password' | 'google' | 'apple' | 'facebook'
 *
 * 'google' here means signing into Primis with a Google account (OAuth sign-in).
 * It is NOT the same as Google Health API authorization, which is tracked in
 * `provider_connections` (CU-028). See TAD §9.2.
 */
export interface AuthIdentitiesTable {
  /** Internal UUID primary key. */
  id: UuidPk;
  /** FK to `users.id`. */
  user_id: string;
  /**
   * App sign-in method.
   * Allowed values: 'email_password' | 'google' | 'apple' | 'facebook'
   */
  provider: string;
  /** Provider-assigned subject identifier (e.g. Google sub claim). */
  provider_subject: string;
  /** Email associated with this identity (may differ from users.email). */
  email: NullableCol<string>;
  linked_at: CreatedAt;
  /** Null until the user has authenticated via this identity at least once. */
  last_used_at: NullableCol<Date>;
}

/** Full row shape returned by SELECT queries on `auth_identities`. */
export type AuthIdentity = Selectable<AuthIdentitiesTable>;
/** Shape accepted by INSERT queries on `auth_identities`. */
export type NewAuthIdentity = Insertable<AuthIdentitiesTable>;
/** Shape accepted by UPDATE queries on `auth_identities`. */
export type AuthIdentityUpdate = Updateable<AuthIdentitiesTable>;

