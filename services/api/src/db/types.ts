/**
 * Append-only log of meaningful consent events.
 *
 * Allowed consent_type values:
 *   'terms' | 'privacy_policy' | 'ai_processing' | 'google_health' |
 *   'healthkit' | 'health_connect' | 'data_retention' | 'marketing'
 *
 * Note: `ip_hash` and `user_agent_hash` are hashed client metadata for audit
 * purposes. Raw IP addresses and user-agent strings must never be stored.
 */
export interface ConsentRecordsTable {
  /** Internal UUID primary key. */
  id: UuidPk;
  /** FK to `users.id`. */
  user_id: string;
  /**
   * Type of consent event. Enforced by DB CHECK constraint.
   */
  consent_type: string;
  /** Policy or document version the user consented to, e.g. '1.0'. */
  version: string;
  /** True if consent was granted; false if declined or revoked. */
  granted: boolean;
  granted_at: CreatedAt;
  /** Set when consent is revoked after having been granted. */
  revoked_at: NullableCol<Date>;
  /** SHA-256 hash of the client IP address (for audit; never the raw IP). */
  ip_hash: NullableCol<string>;
  /** SHA-256 hash of the User-Agent header (for audit; never the raw value). */
  user_agent_hash: NullableCol<string>;
  /** Free-form audit metadata. */
  metadata: Generated<Record<string, unknown>>;
}

/** Full row shape returned by SELECT queries on `consent_records`. */
export type ConsentRecord = Selectable<ConsentRecordsTable>;
/** Shape accepted by INSERT queries on `consent_records`. */
export type NewConsentRecord = Insertable<ConsentRecordsTable>;

